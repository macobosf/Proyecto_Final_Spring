COMMENT ON COLUMN audit_logs.action IS 'Acción técnica realizada.';
COMMENT ON COLUMN audit_logs.resource_type IS 'Tipo de recurso afectado.';
COMMENT ON COLUMN audit_logs.resource_id IS 'Identificador del recurso afectado.';
COMMENT ON COLUMN audit_logs.previous_value IS 'Estado anterior serializado como JSON.';
COMMENT ON COLUMN audit_logs.new_value IS 'Estado nuevo serializado como JSON.';
COMMENT ON COLUMN audit_logs.result IS 'Resultado de la operación: SUCCESS o FAILED.';
COMMENT ON COLUMN audit_logs.ip_address IS 'Dirección IPv4 o IPv6 de origen.';
COMMENT ON COLUMN audit_logs.http_method IS 'Método HTTP utilizado.';
COMMENT ON COLUMN audit_logs.endpoint IS 'Ruta solicitada.';
COMMENT ON COLUMN audit_logs.correlation_id IS 'Identificador para relacionar auditoría y logs técnicos.';
COMMENT ON COLUMN audit_logs.created_at IS 'Fecha de la operación.';

CREATE INDEX idx_audit_logs_actor ON audit_logs(actor_id);
CREATE INDEX idx_audit_logs_action ON audit_logs(action);
CREATE INDEX idx_audit_logs_resource ON audit_logs(resource_type, resource_id);
