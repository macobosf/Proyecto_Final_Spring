-- --------------------------------------------------------------------------
-- 9. REFRESH_TOKENS
-- Se utiliza PostgreSQL para revocación porque Redis queda reservado para
-- bloqueo temporal de login y rate limiting.
-- --------------------------------------------------------------------------
CREATE TABLE refresh_tokens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    token_id UUID NOT NULL,
    user_id BIGINT NOT NULL,
    token_hash VARCHAR(255) NOT NULL,
    expires_at TIMESTAMPTZ NOT NULL,
    revoked_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_by_ip VARCHAR(45),
    replaced_by_token_id UUID,

    CONSTRAINT uq_refresh_tokens_token_id UNIQUE (token_id),
    CONSTRAINT uq_refresh_tokens_token_hash UNIQUE (token_hash),
    CONSTRAINT fk_refresh_tokens_user
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT chk_refresh_tokens_expiration
        CHECK (expires_at > created_at),
    CONSTRAINT chk_refresh_tokens_revocation
        CHECK (revoked_at IS NULL OR revoked_at >= created_at)
);

COMMENT ON TABLE refresh_tokens IS 'Refresh tokens persistidos mediante hash para permitir renovación, rotación, cierre de sesión y revocación del lado del servidor.';
COMMENT ON COLUMN refresh_tokens.id IS 'Identificador interno del refresh token.';
COMMENT ON COLUMN refresh_tokens.token_id IS 'Identificador UUID incluido como jti del token.';
COMMENT ON COLUMN refresh_tokens.user_id IS 'Usuario propietario del token.';
COMMENT ON COLUMN refresh_tokens.token_hash IS 'Hash del refresh token. No se almacena el token original.';
COMMENT ON COLUMN refresh_tokens.expires_at IS 'Fecha de expiración.';
COMMENT ON COLUMN refresh_tokens.revoked_at IS 'Fecha de revocación. Nulo mientras el token está vigente.';
COMMENT ON COLUMN refresh_tokens.created_at IS 'Fecha de emisión.';
COMMENT ON COLUMN refresh_tokens.created_by_ip IS 'IP desde la que se emitió el token.';
COMMENT ON COLUMN refresh_tokens.replaced_by_token_id IS 'Identificador del nuevo token emitido durante una rotación.';

CREATE INDEX idx_refresh_tokens_user ON refresh_tokens(user_id);
CREATE INDEX idx_refresh_tokens_expires_at ON refresh_tokens(expires_at);

