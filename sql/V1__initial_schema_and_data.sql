CREATE TABLE events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(160) NOT NULL,
    description TEXT NOT NULL,
    modality VARCHAR(20) NOT NULL,
    location VARCHAR(200),
    virtual_url VARCHAR(500),
    capacity INTEGER NOT NULL,
    available_capacity INTEGER NOT NULL,
    registration_start_at TIMESTAMPTZ NOT NULL,
    registration_end_at TIMESTAMPTZ NOT NULL,
    start_at TIMESTAMPTZ NOT NULL,
    end_at TIMESTAMPTZ NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'DRAFT',
    organizer_id BIGINT NOT NULL,
    category_id BIGINT NOT NULL,
    deleted BOOLEAN NOT NULL DEFAULT FALSE,
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_events_organizer
        FOREIGN KEY (organizer_id) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT fk_events_category
        FOREIGN KEY (category_id) REFERENCES categories(id) ON DELETE RESTRICT,
    CONSTRAINT chk_events_modality
        CHECK (modality IN ('PRESENTIAL', 'VIRTUAL', 'HYBRID')),
    CONSTRAINT chk_events_status
        CHECK (status IN ('DRAFT', 'PUBLISHED', 'FINISHED', 'CANCELLED')),
    CONSTRAINT chk_events_capacity
        CHECK (capacity > 0),
    CONSTRAINT chk_events_available_capacity
        CHECK (available_capacity BETWEEN 0 AND capacity),
    CONSTRAINT chk_events_date_order
        CHECK (
            registration_start_at < registration_end_at
            AND registration_end_at <= start_at
            AND start_at < end_at
        ),
    CONSTRAINT chk_events_modality_data
        CHECK (
            (modality = 'PRESENTIAL' AND location IS NOT NULL AND virtual_url IS NULL)
            OR (modality = 'VIRTUAL' AND location IS NULL AND virtual_url IS NOT NULL)
            OR (modality = 'HYBRID' AND location IS NOT NULL AND virtual_url IS NOT NULL)
        )
);

COMMENT ON TABLE events IS 'Eventos académicos administrados por organizadores. Incluye cupos, modalidad, fechas, estado y eliminación lógica.';
COMMENT ON COLUMN events.id IS 'Identificador único del evento.';
COMMENT ON COLUMN events.title IS 'Título público del evento.';
COMMENT ON COLUMN events.description IS 'Descripción detallada del evento.';
COMMENT ON COLUMN events.modality IS 'Modalidad: PRESENTIAL, VIRTUAL o HYBRID.';
COMMENT ON COLUMN events.location IS 'Lugar físico requerido para eventos presenciales o híbridos.';
COMMENT ON COLUMN events.virtual_url IS 'Enlace de acceso requerido para eventos virtuales o híbridos.';
COMMENT ON COLUMN events.capacity IS 'Cupo total configurado para el evento.';
COMMENT ON COLUMN events.available_capacity IS 'Cupos todavía disponibles. Solo las inscripciones CONFIRMED consumen cupo.';
COMMENT ON COLUMN events.registration_start_at IS 'Inicio del periodo de inscripciones.';
COMMENT ON COLUMN events.registration_end_at IS 'Fin del periodo de inscripciones.';
COMMENT ON COLUMN events.start_at IS 'Fecha y hora de inicio del evento.';
COMMENT ON COLUMN events.end_at IS 'Fecha y hora de finalización del evento.';
COMMENT ON COLUMN events.status IS 'Estado del evento: DRAFT, PUBLISHED, FINISHED o CANCELLED.';
COMMENT ON COLUMN events.organizer_id IS 'Usuario propietario del evento.';
COMMENT ON COLUMN events.category_id IS 'Categoría a la que pertenece el evento.';
COMMENT ON COLUMN events.deleted IS 'Marca de eliminación lógica.';
COMMENT ON COLUMN events.version IS 'Versión utilizada para control de concurrencia optimista con JPA.';
COMMENT ON COLUMN events.created_at IS 'Fecha de creación del evento.';
